package me.pandamods.pandalib.api.client.screen.layouts;

import net.fabricmc.api.EnvType;
import net.fabricmc.api.Environment;

@Environment(value= EnvType.CLIENT)
public class PLSpacerElement implements PLLayout {
	private int x;
	private int y;
	private int width;
	private int height;

	public PLSpacerElement(int width, int height) {
		this(0, 0, width, height);
	}

	public PLSpacerElement(int x, int y, int width, int height) {
		this.x = x;
		this.y = y;
		this.width = width;
		this.height = height;
	}

	public static PLSpacerElement width(int width) {
		return new PLSpacerElement(width, 0);
	}

	public static PLSpacerElement height(int height) {
		return new PLSpacerElement(0, height);
	}

	@Override
	public void setX(int x) {
		this.x = x;
	}

	@Override
	public void setY(int y) {
		this.y = y;
	}

	@Override
	public int getX() {
		return this.x;
	}

	@Override
	public int getY() {
		return this.y;
	}

	@Override
	public void setWidth(int width) {
		this.width = width;
	}

	@Override
	public void setHeight(int height) {
		this.height = height;
	}

	@Override
	public int getWidth() {
		return this.width;
	}

	@Override
	public int getHeight() {
		return this.height;
	}
}
